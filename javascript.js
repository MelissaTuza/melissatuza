function sumar(){
	var numero1= document.getElementById("num1").value;
	var numero2= document.getElementById("num2").value;
	var resultado = parseFloat(numero1) + parseFloat(numero2);

	alert(resultado);

}
function restar(){

	var numero1= document.getElementById("num1").value;
	var numero2= document.getElementById("num2").value;
	var resultado = parseFloat(numero1) - parseFloat(numero2);

	alert(resultado);

}
function multiplicar(){

	var numero1= document.getElementById("num1").value;
	var numero2= document.getElementById("num2").value;
	var resultado = parseFloat(numero1) * parseFloat(numero2);

	alert(resultado);

}
function dividir(){

	var numero1= document.getElementById("num1").value;
	var numero2= document.getElementById("num2").value;
	var resultado = parseFloat(numero1) / parseFloat(numero2);

	alert(resultado);

}